package ExemplosWHILE;

public class Exercicio4 {

    public static void main(String[] args) {

        int x = 500;
        while (x <= 1000) {
            System.out.println(x);
            x += 100;

        }
        for (int i = 500; i <= 100; i++) {
            System.out.println(i);
        }
    }
}
